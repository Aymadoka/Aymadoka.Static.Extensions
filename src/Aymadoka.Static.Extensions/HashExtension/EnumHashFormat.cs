namespace Aymadoka.Static.HashExtension
{
    /// <summary>表示哈希字符串的格式选项</summary>
    public enum EnumHashFormat
    {
        /// <summary>小写单字符格式（如 "a"）</summary>
        x = 1,

        /// <summary>小写双字符格式（如 "0a"）</summary>
        x2 = 2,

        /// <summary>大写单字符格式（如 "A"）</summary>
        X = 3,

        /// <summary>大写双字符格式（如 "0A"）</summary>
        X2 = 4,
    }
}
